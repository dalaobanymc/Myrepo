package ssm.model;

public class LogiComp {
    private long logiCompId;
    private String logiCompAccount;
    private String logiCompPasswd;

    public long getLogiCompId() {
        return logiCompId;
    }

    public void setLogiCompId(long logiCompId) {
        this.logiCompId = logiCompId;
    }

    public String getLogiCompAccount() {
        return logiCompAccount;
    }

    public void setLogiCompAccount(String logiCompAccount) {
        this.logiCompAccount = logiCompAccount;
    }

    public String getLogiCompPasswd() {
        return logiCompPasswd;
    }

    public void setLogiCompPasswd(String logiCompPasswd) {
        this.logiCompPasswd = logiCompPasswd;
    }
}
