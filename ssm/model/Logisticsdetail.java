package ssm.model;

public class Logisticsdetail {
    private String goodsName;
    private String goodsNum;
    private String MerchantsName;
    private String userName;
    private String courierName;
    private String courierTel;
    private String placeNow;
    private String logisticsState;

    public String getGoodsName() {
        return goodsName;
    }

    public void setGoodsName(String goodsName) {
        this.goodsName = goodsName;
    }

    public String getGoodsNum() {
        return goodsNum;
    }

    public void setGoodsNum(String goodsNum) {
        this.goodsNum = goodsNum;
    }

    public String getMerchantsName() {
        return MerchantsName;
    }

    public void setMerchantsName(String merchantsName) {
        MerchantsName = merchantsName;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getCourierName() {
        return courierName;
    }

    public void setCourierName(String courierName) {
        this.courierName = courierName;
    }

    public String getCourierTel() {
        return courierTel;
    }

    public void setCourierTel(String courierTel) {
        this.courierTel = courierTel;
    }

    public String getPlaceNow() {
        return placeNow;
    }

    public void setPlaceNow(String placeNow) {
        this.placeNow = placeNow;
    }

    public String getLogisticsState() {
        return logisticsState;
    }

    public void setLogisticsState(String logisticsState) {
        this.logisticsState = logisticsState;
    }
}
